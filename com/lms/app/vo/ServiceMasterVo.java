package com.lms.app.vo;

public class ServiceMasterVo
{
  private String srno;
  private String service_desc;
  private String service_short_desc;
  
  public String getSrno()
  {
    return this.srno;
  }
  
  public void setSrno(String srno)
  {
    this.srno = srno;
  }
  
  public String getService_desc()
  {
    return this.service_desc;
  }
  
  public void setService_desc(String service_desc)
  {
    this.service_desc = service_desc;
  }
  
  public String getService_short_desc()
  {
    return this.service_short_desc;
  }
  
  public void setService_short_desc(String service_short_desc)
  {
    this.service_short_desc = service_short_desc;
  }
}
